    <h2>Emerging Journalistic Technologies</h2>
    <p>
      <strong>Definition:</strong> New developments using data and technology.
    </p>
    <ul>
      <li>Drone Journalism</li>
      <li>Sensor Journalism</li>
      <li>Virtual and Augmented Reality Journalism</li>
    </ul>
    <p>
      <strong>Drone Technologies:</strong>
    </p>
    <p>“Drone journalism is generally defined as the use of unmanned aerial systems to gather photos, video and data for news. What separates Drone Journalism from drone photography is the application of journalistic ethics and consideration of the public interest when using [drones].” — Matt Waite, a professor of practice at the University of Nebraska and founder of the Drone Journalism Lab</p>
    <p>Drone technologies can include an airframe, defined by configuration (such as fixed wing or multirotor); an autopilot of varying capabilities (full automation, minor stability assistance, return-to-home fail-safe functionality); a control system (manual control through radio signals, automated flight through software and Bluetooth wireless connection); and a sensor (camera, video camera, multispectral camera, other physical sensor).</p>
    <p>
      <strong>Sensor Technologies:</strong>
    </p>
    <p>Sensor technologies include a wide range of software and hardware to measure physical conditions like air quality, motion, or noise levels. These can be used to gather data with a small, portable computer or microcontroller. The Raspberry Pi is a low-cost, credit card–sized computer that has a variety of input/output pins for mounting devices like sensors. Similarly, Arduino is an open-source microcontroller platform that is widely used for prototyping with electronic components like sensors. Some universities have already begun teaching sensor journalism with specific project-based classes, such as to test environmental conditions like air and water quality.</p>
    <p>
      <strong>Virtual and Augmented Reality Technologies:</strong>
      Virtual reality (VR), long heralded as an emerging digital technology, finally appears poised to enter the broad consumer market. Samsung, Oculus, and Google have developed consumer VR headsets along with controllers to facilitate interactivity using your hands and feet. From a production standpoint, panoramic images and videos may be stitched together from an array of cameras, while the company Jaunt is developing a standalone camera to capture 3D video in 360-degree, immersive format. Yet questions of narrative, audience interaction, and journalistic values have yet to be settled with these technologies, even as the New York Times, Los Angeles Times, and PBS “Frontline” have launched exploratory ventures to use VR. Journalism schools need to not only provide exposure and instruction in this emerging technology, but also to inquire into values and best practices.
    </p>

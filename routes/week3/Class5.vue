    <h2>A brief history of FOIA</h2>
    <p>
      The
      <em>Freedom of Information Act</em>, or FOIA, gives the American people the tools to peer into the workings of the government. The government works for the people, so it’s only reasonable that non-confidential documents and information created by federal agencies be public. Though FOIA seems an essential tenet of American freedom and transparency, it was only signed into law relatively recently in American history.
    </p>
    <p>In the 1950s, California Congressmember John Moss, a huge advocate for government transparency, proposed FOIA. It took more than 10 years, but in 1966 Moss got FOIA signed into law by a reluctant Lyndon B. Johnson.</p>

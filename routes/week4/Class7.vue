    <h2>In-class exercise: Temple Admissions Data</h2>
    <p>Design is an iterative process. The most effective route for many is to sketch out concepts by hand before implementing them. Often the process goes: freehand sketch &rarr; design in Figma or other prototyping tool &rarr; (optional) code it up in HTML/CSS/JS and add interactivity.</p>
    <p>In this exercise, you’re going to do data visualization by drawing on a piece of paper. I’ve included links to recent Temple University admissions data below. Your task is to pick out any kind of stat or stats and draw a visualization for it. You do not have to be perfectly accurate with where you draw things (bars, dots, whatever visualization you choose), but attempt to be precise, and don’t use a computer to aid you. You can select data for a single class year, aggregate things across class years, or use your own data if you really want.</p>

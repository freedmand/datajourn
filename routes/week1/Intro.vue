    <h4>What is data journalism?</h4>
    <p>Journalism that involves data. Finding data, extracting stories from data, presenting visualizations backed by data.</p>
    <h4>Ok, so what exactly is data?</h4>
    <p>In its essence, data is information about the world. It can take the shape of the number in attendance at political inaugurations, the results from polls or surveys, networks of connections extracted from government documents, and just about anything else you can think of. Data becomes interesting at large scales, when you have a lot of data points and are able to find trends and outliers.</p>
    <h4>Why now?</h4>
    <p>Data journalism is a field that’s just starting to get big now. We are producing more information than ever, and computers are getting faster and faster. We are at a time where there is an unprecedented scale of being able to automate tasks, crunch numbers, and pull data from different sources.</p>
    <p>When you combine a journalistic sense of getting to the bottom of a story with the ability to comb through tons of data computationally, you can bring about stories that have never been possible before.</p>

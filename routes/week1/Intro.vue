    <h4>Computer-assisted reporting</h4>
    <p>
      The field of data journalism goes by a number of names. The earliest name was probably “computer-assisted reporting,” referring to using computing to aid in the reporting process. The practice of combining journalists and computers to tell stories goes back to at least the 1960s, when data guru Philip Meyer of the Detroit Free Press used a computer to show that rioters spanned the age spectrum. Meyer insists in his book
      <em>Precision Journalism</em> (available freely
      <a href="https://carolinadatadesk.github.io/pmeyer/book/">online</a>) that it’s no longer enough as a journalist to just be a truth-seeking writer:
    </p>
    <blockquote>
      <em>There was a time when all it took was a dedication to truth, plenty of energy, and some talent for writing. You still need those things, but they are no longer sufficient. The world has become so complicated, the growth of available information so explosive, that the journalist needs to be a filter as well as a transmitter, an organizer and interpreter as well as one who gathers and delivers facts. In addition to knowing how to get information into print or on the air, he or she also must know how to get it into the receiver's head. In short, a journalist has to be a database manager, a data processor, and a data analyst.</em>
    </blockquote>
    <h4>Computational journalism</h4>
    <p>
      <a
        href="http://cacm.acm.org/magazines/2011/10/131400-computational-journalism/fulltext"
      >An academic paper</a> by computational journalist pioneers Sarah Cohen, James T. Hamilton, and Fred Turner provides the following definition for computational journalism:
      <em>“Broadly defined, [computational journalism] can involve changing how stories are discovered, presented, aggregated, monetized, and archived. Computation can advance journalism by drawing on innovations in topic detection, video analysis, personalization, aggregation, visualization, and sensemaking.”</em>
    </p>
    <p>When I was a journalism student at Stanford, Hamilton provided the following more concise definition:</p>
    <blockquote>
      <em>
        Computational journalism is stories
        <b>by, through, and about</b> algorithms.
      </em>
    </blockquote>
    <p>Let‘s dissect that definition. Algorithms are computational methods or routines.</p>
    <ul>
      <li>
        <em>
          <b>Stories by algorithms?</b>
        </em> Well, some stories are already being written by computers, mainly those that follow predictable recipes, e.g. sports games and stocks.
      </li>
      <li>
        <em>
          <b>Stories through algorithms?</b>
        </em> That’s more in the spirit of computer-assisted reporting: using computational methods to find, filter, clean, and extract data in the service of telling stories.
      </li>
      <li>
        <em>
          <b>Stories about algorithms?</b>
        </em> Well, mysterious computer code is responsible for many consequential parts of modern society: Facebook’s algorithm mysteriously curates content and purportedly holds back fake news. Or does it? And how do credit scores work? Do they disadvantage certain groups of people?
      </li>
    </ul>
    <p>As computers increasingly run our lives, computational journalism captures the essence of computer-assisted reporting but also looks at holding accountable the companies and institutions responsible for the code that runs our society.</p>
    <h4>Data journalism</h4>
    <p>Ok, you’re probably thoroughly confused by now. The point I’m trying to make is that these terms are mostly interchangeable. There have been many attempts by well-meaning people to capture this emerging field with a catchy name. There’s a rich history behind all the different names computational and data-driven journalism can take, but the important thing to know is that they mostly refer to the same thing.</p>
    <p>In this course, we will focus on understanding data and using the computer to collect, filter, clean, and pull stories from it. We will look at using data and design principles to create compelling and beautiful visualizations. And we will learn the basics of coding for creating content on the web and scraping information. We’ll also discuss some of the nuances of the field and emerging trends, like artificial intelligence and the ethical problems in data and AI.</p>
    <p>The key is not to learn everything, but to be exposed to a wide surface area and learn how to teach yourself towards mastery.</p>

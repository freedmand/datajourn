    <h4>Overview</h4>
    <p>This course explores the emerging field of data journalism. We start by analyzing what data journalism means and what goes into successful data-driven stories. Then, we will dive into three big themes of data journalism: data, visualization, and code. We will look at how to find data, request public records, clean up datasets, and extract what’s newsworthy from a dataset. On the visual side, we will learn about design principles, the power of data visualizations, and tools to create visualizations. Finally, we study coding: how to create basic websites, scrape data, put together interactives, and use search effectively. The course ends with discussions on where the field is heading, news applications, and the joys and dangers in artificial intelligence.</p>

